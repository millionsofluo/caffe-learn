/*
	通过对caffe损失层的研究，发现，前向传播阶段，数据逐层传播，
	到损失层计算预测概率密度和损失函数。
	反向传播阶段则是从损失层开始，由概率密度和理想概率密度（有监督学习）
	的差值得到diff（误差），然后逐层反向传播
	
	我们知道一个blob由data和daff两部分组成，
	数据读取层是data之源
	损失层就是diff之源
	*/